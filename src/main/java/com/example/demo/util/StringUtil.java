package com.example.demo.util;

public class StringUtil {

    /**
     * 文字列の前後の空白を取り除き、結果が空文字なら null を返すユーティリティメソッド
     *
     * 【仕様】
     * - null が渡されたら null のまま返す
     * - 前後を trim() で削除する
     * - trim 後の文字列が空（""）なら null を返す
     * - それ以外はトリムされた文字列をそのまま返す
     *
     * よくある用途：
     * - フォーム入力の値を扱うとき
     * - 空白だけの入力を「空扱い(null扱い)」したい場合
     */
    public static String trimToNull(String s) {

        // ----------------------------------------------------
        // ① 入力が null の場合はそのまま null を返す
        //    ※ null チェックを先にすることで NPE を防ぐ
        // ----------------------------------------------------
        if (s == null) return null;

        // ----------------------------------------------------
        // ② 前後の空白（スペース、タブ、改行など）を削除する
        // ----------------------------------------------------
        String trimmed = s.trim();

        // ----------------------------------------------------
        // ③ trim の結果が空文字("")なら null を返す
        //    → "   " のように空白のみの入力を「未入力扱い」にできる
        // ----------------------------------------------------
        return trimmed.isEmpty() ? null : trimmed;
    }
}


